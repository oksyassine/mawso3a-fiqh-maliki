def get_zakat_stats():
    """Print summary statistics."""
    total = len(ZAKAT_DATA)
    ittifaq = sum(1 for m in ZAKAT_DATA if m["comparison"]["result"] == "ittifaq")
    ikhtilaf = sum(1 for m in ZAKAT_DATA if m["comparison"]["result"] == "ikhtilaf")
    tafsilat = sum(1 for m in ZAKAT_DATA if m["comparison"]["result"] == "tafsilat")
    positions = sum(len(m["positions"]) for m in ZAKAT_DATA)

    categories = {}
    for m in ZAKAT_DATA:
        cat = m["category"]
        categories[cat] = categories.get(cat, 0) + 1

    print(f"كتاب الزكاة — Kitab al-Zakat")
    print(f"================================")
    print(f"المسائل: {total}")
    print(f"المواقف المستخرجة: {positions}")
    print(f"اتفاق: {ittifaq}")
    print(f"اختلاف: {ikhtilaf}")
    print(f"تفصيلات: {tafsilat}")
    print()
    print("التوزيع حسب الأبواب:")
    for cat, count in categories.items():
        print(f"  {cat}: {count}")


if __name__ == "__main__":
    get_zakat_stats()
