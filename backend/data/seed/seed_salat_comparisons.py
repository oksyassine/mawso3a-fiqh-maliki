def get_salat_stats():
    """Print summary statistics."""
    total = len(SALAT_DATA)
    ittifaq = sum(1 for m in SALAT_DATA if m["comparison"]["result"] == "ittifaq")
    ikhtilaf = sum(1 for m in SALAT_DATA if m["comparison"]["result"] == "ikhtilaf")
    tafsilat = sum(1 for m in SALAT_DATA if m["comparison"]["result"] == "tafsilat")
    positions = sum(len(m["positions"]) for m in SALAT_DATA)

    categories = {}
    for m in SALAT_DATA:
        cat = m["category"]
        categories[cat] = categories.get(cat, 0) + 1

    print(f"كتاب الصلاة — Kitab al-Salat")
    print(f"================================")
    print(f"المسائل: {total}")
    print(f"المواقف المستخرجة: {positions}")
    print(f"اتفاق: {ittifaq}")
    print(f"اختلاف: {ikhtilaf}")
    print(f"تفصيلات: {tafsilat}")
    print()
    print("التوزيع حسب الأبواب:")
    for cat, count in categories.items():
        print(f"  {cat}: {count}")


if __name__ == "__main__":
    get_salat_stats()
