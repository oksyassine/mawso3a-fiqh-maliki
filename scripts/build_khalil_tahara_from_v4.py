# === BUILD ===
masail_out = []
for i, mdef in enumerate(MASAIL):
    sec_name, sec_entries = section_list[mdef['sec_idx']]
    sel_entries = [sec_entries[j] for j in mdef['entries']]

    # Concatenate all entries into one continuous text (pages split mid-sentence)
    texts = [entry.get('khalil_text', entry.get('text', '')) for entry in sel_entries]
    full_text = '\n'.join(texts)
    first_page = sel_entries[0].get('page_id')
    last_page = sel_entries[-1].get('page_id')
    # خليل is pure متن — the ruling IS the dalil (no separate hadiths)
    dalil = [{
        'type': 'متن',
        'full_text': full_text,
        'page_id': first_page,
        'page_end': last_page,
        'shamela_url': shamela_url(first_page) if first_page else '',
    }]

    masail_out.append({
        'masala_id': f'khalil_tahara_{i+1:03d}',
        'bab': sec_name,
        'bab_num': mdef['sec_idx'] + 1,
        'masala_topic': mdef['topic'],
        'hukm': mdef['hukm'],
        'rulings': [{
            'speaker': 'خليل',
            'text': mdef['ruling'],
        }],
        'dalil': dalil,
        'fiqh_tags': mdef['tags'],
        'sub_masail': [],
        '_source': {
            'page_start': first_page,
            'page_end': last_page,
            'shamela_url': shamela_url(first_page) if first_page else '',
            'full_text': full_text,
        },
    })

