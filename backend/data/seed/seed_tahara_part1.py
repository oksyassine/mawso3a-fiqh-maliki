def get_tahara_part1_stats():
    """Print summary statistics for Tahara Part 1."""
    total = len(TAHARA_PART1)
    ittifaq = sum(1 for m in TAHARA_PART1 if m["comparison"]["result"] == "ittifaq")
    ikhtilaf = sum(1 for m in TAHARA_PART1 if m["comparison"]["result"] == "ikhtilaf")
    tafsilat = sum(1 for m in TAHARA_PART1 if m["comparison"]["result"] == "tafsilat")
    positions = sum(len(m["positions"]) for m in TAHARA_PART1)

    categories = {}
    for m in TAHARA_PART1:
        cat = m["category"]
        categories[cat] = categories.get(cat, 0) + 1

    print(f"كتاب الطهارة — النصف الأول (Part 1)")
    print(f"=" * 50)
    print(f"المسائل الكلية: {total}")
    print(f"المواقف المستخرجة: {positions}")
    print(f"المعدل: {positions / total:.1f} موقف/مسألة")
    print()
    print(f"النتائج:")
    print(f"  اتفاق: {ittifaq}")
    print(f"  اختلاف: {ikhtilaf}")
    print(f"  تفصيلات: {tafsilat}")
    print()
    print(f"التوزيع حسب الأبواب:")
    for cat, count in sorted(categories.items(), key=lambda x: -x[1]):
        print(f"  {cat}: {count} مسألة")


if __name__ == "__main__":
    get_tahara_part1_stats()
